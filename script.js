const player = document.getElementById("player");
const playerContent = document.getElementById("playerContent");
const playBtn = document.getElementById("play");
const closeBtn = document.getElementById("close");

let currentVideo = null;

/* 🔍 DETECTAR TIPO DE VIDEO */
function getEmbedURL(url){

    // GOOGLE DRIVE
    if(url.includes("drive.google.com")){
        const match = url.match(/\/d\/(.*?)\//);
        if(match){
            return {
                type:"iframe",
                src:`https://drive.google.com/file/d/${match[1]}/preview`
            };
        }
    }

    // YOUTUBE
    if(url.includes("youtube.com") || url.includes("youtu.be")){
        const match = url.match(/(v=|youtu\.be\/)([^&]+)/);
        if(match){
            return {
                type:"iframe",
                src:`https://www.youtube.com/embed/${match[2]}`
            };
        }
    }

	// MP4 (con optimización Cloudinary)
	if(url.endsWith(".mp4")){
	    let optimizedURL = url.replace(
	        "/upload/",
	        "/upload/q_auto,f_auto,vc_auto/"
	    );

	    return {
	        type:"video",
	        src: optimizedURL
	    };
	}

    // MEGA EMBED
    if(url.includes("mega.nz/embed")){
        return {
            type:"iframe",
            src:url
        };
    }

    return null;
}

/* 🎬 ABRIR PLAYER */
function openPlayer(src){

    if(src.includes("mega.nz") && !src.includes("embed")){
        window.open(src, "_blank");
        return;
    }

    const videoData = getEmbedURL(src);

    if(!videoData){
        alert("❌ No compatible");
        return;
    }

    player.classList.remove("hidden");
    playerContent.innerHTML = "";

    if(videoData.type === "video"){
        playerContent.innerHTML = `
        <video id="video" controls controlsList="nodownload noplaybackrate noremoteplayback" preload="metadata">
            <source src="${videoData.src}" type="video/mp4">
        </video>`;
        
        currentVideo = document.getElementById("video");

        // 🚫 Bloquear clic derecho
		currentVideo.addEventListener("contextmenu", e => e.preventDefault());

    }else{
        playerContent.innerHTML = `
        <iframe src="${videoData.src}" allow="autoplay" allowfullscreen></iframe>`;
        
        currentVideo = null;
    }
}

/* ▶ PLAY / PAUSE (solo MP4) */
playBtn.onclick = ()=>{
    if(!currentVideo) return;

    if(currentVideo.paused){
        currentVideo.play();
        playBtn.textContent="⏸";
    }else{
        currentVideo.pause();
        playBtn.textContent="▶";
    }
};

